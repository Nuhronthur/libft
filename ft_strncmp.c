#include "libft.h"

int	ft_strncmp(const char *f, const char *s, unsigned int l)
{
	int	c;

	c = 0;
	while (c <= l)
	{
		if (f[c] != s[c])
			return (f[c] - s[c]);
		c++;
	}
}
