#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*str;

	str = (unsigned char *) s;
	while (s)
	{
		if (str == (unsigned char *)c)
			return (str);
		str++;
	}
	return (NULL);
}
